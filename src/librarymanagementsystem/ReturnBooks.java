    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String s1=reg.getText();
        String s2=sname.getText();
        String s3=bookid.getText();
        String s4=bookname.getText();
        String s5=author.getText();
        String s6=((JTextField)issuedate.getDateEditor().getUiComponent()).getText();
        String s7=returndate.getText();
        try{
            if(s1.length()==0 || s2.length()==0 || s3.length()==0 || s4.length()==0 || s5.length()==0 || s6.length()==0 || s7.length()==0)
            {
                
                JOptionPane.showMessageDialog(null, "Some fields are EMPTY !!!");
            }
            else
            {Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library_management","root","");
            PreparedStatement ps = con.prepareStatement("insert into returnedbooks values(?,?,?,?,?,?,?)");          
                    ps.setString(1, s1);
                    ps.setString(2, s2);
                    ps.setString(3, s3);
                    ps.setString(4, s4);
                    ps.setString(5, s5);
                    ps.setString(6, s6);
                    ps.setString(7, s7);
                    ps.executeUpdate();
                    {  
                        JOptionPane.showMessageDialog(null, "Book is returned Successfully");  
                    }
            } 
        }
         catch (Exception ex)   
                {  
                    System.out.println(ex);  
                }         // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

